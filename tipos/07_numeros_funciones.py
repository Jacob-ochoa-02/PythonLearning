import math

print(round(1.3))  # redondea el valor más cercano
print(round(1.7))  # redondea el valor más cercano
print(round(1.5))  # redondea el valor más cercano
print(abs(-40))  # valor absoluto
print(abs(55))  # valor absoluto

print(math.ceil(1.1))   # Numero superior más cercano
print(math.ceil(19.05))
print(math.floor(1.9999))   # Numero inferior más cercano
print(math.isnan(23))   # Se muestra si el numero no es un numero
print(math.isnan(23))
# print(math.isnan("23")) Muestra el error
print(math.pow(10, 3))  # Se usa la potencia de 10^3
print(math.sqrt(9))  # Se usa para hayar la raiz cuadrada de un numero
